"""Module for loading and preprocessing source code grammar error datasets.

This module provides a hierarchy of PyTorch Dataset classes tailored for three
distinct architectures: Sequence-to-Sequence, Causal Language Modeling, and
Masked Language Modeling. All classes expect JSONL files generated by the
typo_injector pipeline.
"""

import json
from typing import Dict, List, Union, Any

import torch
from torch.utils.data import Dataset
from transformers import PreTrainedTokenizer


class BaseTypoDataset(Dataset):
    """Base dataset class for reading and storing JSONL files.

    This class handles disk I/O, merging multiple file paths, and basic
    validation. It serves as a parent class and should not be instantiated
    directly for training.

    Attributes:
        tokenizer (PreTrainedTokenizer): The Hugging Face tokenizer instance.
        max_length (int): Maximum sequence length for the model input.
        data (List[Dict[str, Any]]): Parsed JSON lines stored in memory.
    """

    def __init__(
        self,
        jsonl_paths: Union[str, List[str]],
        tokenizer: PreTrainedTokenizer,
        max_length: int = 512,
    ) -> None:
        """Initializes the base dataset.

        Args:
            jsonl_paths (Union[str, List[str]]): Path or list of paths to JSONL files.
            tokenizer (PreTrainedTokenizer): Tokenizer specific to the chosen model.
            max_length (int, optional): Max token limit. Defaults to 512.
        """
        self.tokenizer = tokenizer
        self.max_length = max_length
        self.data: List[Dict[str, Any]] = []

        if isinstance(jsonl_paths, str):
            jsonl_paths = [jsonl_paths]

        for path in jsonl_paths:
            with open(path, "r", encoding="utf-8") as f:
                for line in f:
                    self.data.append(json.loads(line))

    def __len__(self) -> int:
        """Returns the total number of samples in the dataset.

        Returns:
            int: The size of the dataset.
        """
        return len(self.data)

    def __getitem__(self, idx: int) -> Dict[str, torch.Tensor]:
        """Retrieves and processes a single sample from the dataset.

        Args:
            idx (int): Index of the desired sample.

        Returns:
            Dict[str, torch.Tensor]: A dictionary containing model-ready tensors
            (`input_ids`, `attention_mask`, `labels`).
        """
        item = self.data[idx]
        return self._process_item(item)

    def _process_item(self, item: Dict[str, Any]) -> Dict[str, torch.Tensor]:
        """Processes a raw dictionary item into model inputs.

        This method must be overridden by subclasses to provide architecture-specific
        tensor formatting.

        Args:
            item (Dict[str, Any]): Raw data dictionary containing 'code' and 'fixed' keys.

        Raises:
            NotImplementedError: If the subclass does not implement this method.

        Returns:
            Dict[str, torch.Tensor]: Processed tensors.
        """
        raise NotImplementedError("Subclasses must implement _process_item method.")


class Seq2SeqTypoDataset(BaseTypoDataset):
    """Dataset for Encoder-Decoder architectures (e.g., CodeT5, BART).

    Prepares data for machine translation tasks, mapping corrupted source
    code to its fixed counterpart.
    """

    def _process_item(self, item: Dict[str, Any]) -> Dict[str, torch.Tensor]:
        """Formats the sample for sequence-to-sequence training.

        Args:
            item (Dict[str, Any]): Raw data dictionary.

        Returns:
            Dict[str, torch.Tensor]: Dictionary containing:
                - 'input_ids': Tokenized corrupted code.
                - 'attention_mask': Attention mask for the encoder.
                - 'labels': Tokenized fixed code with padding replaced by -100.
        """
        source_code = item["code"]
        # If no errors exist, the model should learn to output the code unmodified
        target_code = item.get("fixed") or source_code

        model_inputs = self.tokenizer(
            source_code,
            max_length=self.max_length,
            padding="max_length",
            truncation=True,
            return_tensors="pt",
        )

        labels = self.tokenizer(
            text_target=target_code,
            max_length=self.max_length,
            padding="max_length",
            truncation=True,
            return_tensors="pt",
        )

        labels_ids = labels["input_ids"].squeeze()
        labels_ids[labels_ids == self.tokenizer.pad_token_id] = -100

        return {
            "input_ids": model_inputs["input_ids"].squeeze(),
            "attention_mask": model_inputs["attention_mask"].squeeze(),
            "labels": labels_ids,
        }
